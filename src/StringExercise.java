public class StringExercise {
    public static void main(String[] args) {
        String sentence1 = "We don't need no eduction";
        System.out.println(sentence1);

        String sentence2 = "We don't need no thought control";
        System.out.println(sentence2);

        String sentence3 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(sentence3);

        String sentence4 = "In windows, the main drive is usually C:\\";
        System.out.println(sentence4);

        String sentence5 = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(sentence5);
    }
}

